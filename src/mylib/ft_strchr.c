#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	if ((unsigned char)*s == c)
		return ((char *)s);
	while (*(s++))
		if ((unsigned char)*s == c)
			return ((char *)s);
	return (NULL);
}